# BUCLE WHILE

# Este bucle te permite repetir un bloque de código mientras se cumpla una condición específica.
# El código dentro del bucle while se ejecutará una y otra vez mientras la condición sea verdadera.

# Sintaxis del bucle While
# Ejemplo con un contador:
contador = 1

while contador <= 5:
    print(f"El contador es: {contador}")
    contador += 1   # Incrementa el contador en 1 cada iteración

print("¡Bucle completado!")


# ERRORES
# Hay que tener cuidado con que se nos haga bucles infinitos.
# Por ejemplo si en el anterior ejemplo no colocara la suma del contador, el bucle se repetiría infinitamente.
