# OPERADORES ARITMÉTICOS

# Los operadores aritméticos son los que se utilizan para realizar operaciones matemáticas en Python.
# Sirven para hacer cálculos matemáticos con variables y valores dentro del código.

# Suma y Resta (+ y -).
suma = 12 + 5
resta = 12 - 5

# Multiplicación y División (* y /).
multi = 12 * 5
division = 12 / 5   # Nos devuelve un dato float (racional).

# Potenciación ( ** ).
exponente = 12 ** 5

# División baja ( // ) "doble división".
division_baja = 12 // 5   # Devuelve el número entero y redondeado hacia abajo.

# Resto o módulo ( % ).
resto = 12 % 3   # Nos da el valor de lo que resta de la operación.
