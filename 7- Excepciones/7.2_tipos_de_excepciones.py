# TIPOS DE EXCEPCIONES

# Python proporciona una amplia variedad de tipos de excepciones predefinidas para manejar diferentes situaciones de error.
# Existen más tipos de excepciones que cubren diversas situaciones de error, y también puedes crear tus propias excepciones personalizadas mediante la definición de clases que hereden de la clase "Exception".
# Algunas de estas excepciones más comunes que trae Python son:

# "SyntaxError"
# Esta excepción se produce cuando hay un error en la sintaxis del código, lo que significa que Python no puede entender el código correctamente.

# "IndentationError"
# Esta excepción se produce cuando hay errores en la indentación del código, lo que afecta a la estructura de bloques.

# "NameError"
# Se produce cuando intentas acceder a una variable o nombre que no ha sido definido en el ámbito actual.

# "TypeError"
# Esta excepción ocurre cuando se realiza una operación en objetos de tipos incompatibles.

# "ValueError"
# Se lanza cuando una función recibe un argumento del tipo correcto, pero con un valor incorrecto.

# "ZeroDivisionError"
# Se produce cuando intentas dividir un número entre cero.

# "IndexError"
# Esta excepción se lanza cuando intentas acceder a un índice que está fuera del rango de una secuencia (como una lista o una cadena).

# "FileNotFoundError"
# Se produce cuando intentas abrir o manipular un archivo que no existe en el sistema.

# "KeyError"
# Se lanza cuando intentas acceder a una clave que no existe en un diccionario.

# "AttributeError"
# Se produce cuando intentas acceder a un atributo o método que no existe en un objeto.
